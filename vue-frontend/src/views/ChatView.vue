    }
  }
  
  // Â¶ÇÊûúÁî®Êà∑ÂÖ≥Èó≠‰∫ÜRAGÔºåÂàõÂª∫‰∏Ä‰∏™‰∏çÂ∏¶RAGÂäüËÉΩÁöÑÊñá‰ª∂ÂâØÊú¨
  if (fileToSend && !ragEnabled.value) {
    fileToSend = {
      ...fileToSend,
      rag_enabled: false,
      doc_id: undefined
    }
  }

  await sendMessage(inputMessage.value, fileToSend || undefined)
  inputMessage.value = ''
}

